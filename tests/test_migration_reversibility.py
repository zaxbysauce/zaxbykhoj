"""
Tests for Django migration reversibility.

These tests verify that migrations 0100, 0101, and 0102 can be reversed properly.
Django generates reverse operations automatically for AddField, but we verify
the migrations declare the reversible() method correctly.
"""

import sys
from pathlib import Path
from unittest.mock import MagicMock, patch

import pytest

# Add the src directory to the path for importing the migration
khoj_repo_path = Path(__file__).parent.parent / "khoj-repo" / "src"
sys.path.insert(0, str(khoj_repo_path))


class TestMigration0100Reversible:
    """Test suite for migration 0100_add_search_vector reversibility"""

    @pytest.fixture
    def migration_path(self):
        """Return the path to the migration file."""
        return (
            Path(__file__).parent.parent
            / "khoj-repo"
            / "src"
            / "khoj"
            / "database"
            / "migrations"
            / "0100_add_search_vector.py"
        )

    def test_migration_reversible_method_exists(self, migration_path):
        """Test that the migration declares a reversible() method."""
        with open(migration_path, "r") as f:
            content = f.read()

        # Verify reversible method exists
        assert "def reversible(self):" in content, "Migration should declare reversible() method"
        assert "return True" in content, "reversible() should return True"

    def test_migration_has_addfield_operation(self, migration_path):
        """Test that migration uses AddField which Django can auto-reverse."""
        with open(migration_path, "r") as f:
            content = f.read()

        # Verify AddField operation exists
        assert "migrations.AddField(" in content, "Migration should use AddField operation"
        assert 'model_name="entry"' in content
        assert 'name="search_vector"' in content

    def test_migration_has_addindex_operation(self, migration_path):
        """Test that migration uses AddIndex which Django can auto-reverse."""
        with open(migration_path, "r") as f:
            content = f.read()

        # Verify AddIndex operation exists
        assert "migrations.AddIndex(" in content, "Migration should use AddIndex operation"
        assert "entry_search_vector_gin_idx" in content

    def test_reverse_operations_would_be_generated(self, migration_path):
        """Test that reverse operations would be generated for all forward operations."""
        with open(migration_path, "r") as f:
            content = f.read()

        # Count forward operations that Django can auto-reverse
        add_field_count = content.count("migrations.AddField(")
        add_index_count = content.count("migrations.AddIndex(")

        # Each AddField -> RemoveField (auto-generated by Django)
        # Each AddIndex -> RemoveIndex (auto-generated by Django)
        assert add_field_count >= 1, "Should have at least one AddField"
        assert add_index_count >= 1, "Should have at least one AddIndex"

    def test_migration_file_valid_python(self, migration_path):
        """Test that the migration file is valid Python syntax."""
        with open(migration_path, "r") as f:
            content = f.read()

        try:
            compile(content, str(migration_path), "exec")
        except SyntaxError as e:
            pytest.fail(f"Migration file has syntax error: {e}")


