class TestReverseOperationsExist:
    """Test suite to verify RemoveField operations would be generated for reversals"""

    def test_all_migrations_use_auto_reversible_operations(self):
        """Test that all migrations use operations Django can auto-reverse."""
        migrations_dir = (
            Path(__file__).parent.parent
            / "khoj-repo"
            / "src"
            / "khoj"
            / "database"
            / "migrations"
        )

        target_migrations = [
            "0100_add_search_vector.py",
            "0101_add_context_summary.py",
            "0102_add_chunk_scale.py",
        ]

        for migration_name in target_migrations:
            migration_path = migrations_dir / migration_name

            with open(migration_path, "r") as f:
                content = f.read()

            # Verify only auto-reversible operations are used
            # AddField and AddIndex are auto-reversible by Django
            assert "migrations.AddField(" in content, f"{migration_name} should use AddField"

            # Verify no irreversible operations (like RunPython without reverse_code)
            if "migrations.RunPython(" in content:
                # If RunPython is used, it must have reverse_code
                assert "reverse_code=" in content, \
                    f"{migration_name} uses RunPython without reverse_code"

    def test_migration_dependencies_allow_sequential_reversal(self):
        """Test that migrations can be reversed in sequence."""
        migrations_dir = (
            Path(__file__).parent.parent
            / "khoj-repo"
            / "src"
            / "khoj"
            / "database"
            / "migrations"
        )

        # Check dependency chain allows sequential reversal
        # 0102 depends on 0101, 0101 depends on 0100
        # So to reverse all: migrate to 0099 (reverses 0100, 0101, 0102)

        m0100_path = migrations_dir / "0100_add_search_vector.py"
        m0101_path = migrations_dir / "0101_add_context_summary.py"
        m0102_path = migrations_dir / "0102_add_chunk_scale.py"

        with open(m0100_path, "r") as f:
            m0100_content = f.read()
        with open(m0101_path, "r") as f:
            m0101_content = f.read()
        with open(m0102_path, "r") as f:
            m0102_content = f.read()

        # Verify dependency chain
        assert '("database", "0099_usermemory")' in m0100_content
        assert '("database", "0100_add_search_vector")' in m0101_content
        assert '("database", "0101_add_context_summary")' in m0102_content

    def test_reversible_method_returns_true(self):
        """Test that all migrations have reversible() returning True."""
        migrations_dir = (
            Path(__file__).parent.parent
            / "khoj-repo"
            / "src"
            / "khoj"
            / "database"
            / "migrations"
        )

        target_migrations = [
            "0100_add_search_vector.py",
            "0101_add_context_summary.py",
            "0102_add_chunk_scale.py",
        ]

        for migration_name in target_migrations:
            migration_path = migrations_dir / migration_name

            with open(migration_path, "r") as f:
                content = f.read()

            # Verify reversible method exists and returns True
            assert "def reversible(self):" in content, \
                f"{migration_name} should declare reversible() method"

            # Check that reversible returns True (not False or missing return)
            reversible_start = content.find("def reversible(self):")
            reversible_end = content.find("\n\n", reversible_start)
            reversible_section = content[reversible_start:reversible_end]

            assert "return True" in reversible_section, \
                f"{migration_name} reversible() should return True"


